import sanitizeHtml from "sanitize-html";

export function sanitizeContent(html: string): string {
  return sanitizeHtml(html, {
    allowedTags: sanitizeHtml.defaults.allowedTags.concat([
      "img",
      "iframe",
      "h1",
      "h2",
      "h3",
      "h4",
      "figure",
      "figcaption",
      "br",
      "span",
      "video",
      "table",
      "thead",
      "tbody",
      "tfoot",
      "tr",
      "th",
      "td",
      "caption",
      "col",
      "colgroup",
    ]),
    allowedAttributes: {
      ...sanitizeHtml.defaults.allowedAttributes,
      img: ["src", "alt", "style", "width", "height"],
      iframe: ["src", "style", "allow", "allowfullscreen"],
      div: ["style", "contenteditable"],
      span: ["style"],
      // 블록 요소에도 style 허용 — text-align 등 정렬 인라인 스타일이 살아남아야
      // 빠른편집/풀편집에서 적용한 정렬이 DB → 공개 렌더까지 보존된다.
      // 허용 가능한 속성 값은 allowedStyles 의 화이트리스트로 별도 제한.
      p: ["style"],
      h1: ["style"],
      h2: ["style"],
      h3: ["style"],
      h4: ["style"],
      h5: ["style"],
      h6: ["style"],
      blockquote: ["style"],
      ul: ["style"],
      ol: ["style"],
      li: ["style"],
      figure: ["style"],
      figcaption: ["style"],
      table: ["style", "border", "cellpadding", "cellspacing"],
      tr: ["style"],
      td: ["colspan", "rowspan", "style", "align", "valign"],
      th: ["colspan", "rowspan", "style", "align", "valign", "scope"],
      col: ["span", "style"],
      colgroup: ["span", "style"],
      "*": ["class"],
    },
    allowedStyles: {
      "*": {
        "position": [/^relative$|^absolute$/],
        "top": [/^-?\d+/],
        "left": [/^-?\d+/],
        "width": [/^\d+/],
        "height": [/^\d+/],
        "max-width": [/^\d+/],
        "padding": [/^\d+/],
        "padding-bottom": [/^\d+/],
        "overflow": [/^hidden$/],
        "margin": [/^-?\d+/],
        "border": [/^[\d.]+(px|em|rem)?\s+(solid|dashed|dotted|double|none)/, /^0$|^none$/],
        "border-color": [/^#[0-9a-fA-F]{3,8}$|^rgb/],
        "border-style": [/^(solid|dashed|dotted|double|none)$/],
        "border-width": [/^\d+/],
        "border-collapse": [/^(collapse|separate)$/],
        "border-spacing": [/^\d+/],
        "border-radius": [/^\d+/],
        "background-color": [/^#[0-9a-fA-F]{3,8}$|^rgb/],
        "color": [/^#[0-9a-fA-F]{3,8}$|^rgb/],
        "font-size": [/^\d+/],
        "font-weight": [/^\d+$|^bold$|^normal$/],
        "font-style": [/^(italic|normal|oblique)$/],
        "text-align": [/^left$|^center$|^right$|^justify$/],
        "vertical-align": [/^(top|middle|bottom|baseline)$/],
      },
    },
    allowedIframeHostnames: ["www.youtube.com", "player.vimeo.com"],
    allowedSchemes: ["http", "https", "mailto"],
  });
}
